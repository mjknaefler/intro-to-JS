//Addition
let priceOfMilk = 10;
let priceOfEggs = 20;

let total = priceOfEggs + priceOfMilk;

console.log(total);

//Subtraction
let priceOfBread = 5;
let discountAmount = 2;

let totalBread = priceOfBread - discountAmount;
console.log(totalBread);

//Multiplication
let quantity = 10;
let price  = 5;
let priceTotal = quantity * price;
console.log(priceTotal);

//Division
let totalStudents = 100;
let classRooms = 5;
let studentsPerClassroom = totalStudents / classRooms;
console.log(studentsPerClassroom);

//Modulus %
let remainder = totalStudents % classRooms;
console.log(remainder);
